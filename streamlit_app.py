def request_gpt(system_prompt: str, user_prompt: str) -> str:
    """
    –û—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –∑–∞–ø—Ä–æ—Å –∫ OpenAI GPT API.

    Args:
        system_prompt: –°–∏—Å—Ç–µ–º–Ω—ã–π –ø—Ä–æ–º–ø—Ç, –æ–ø—Ä–µ–¥–µ–ª—è—é—â–∏–π –ø–æ–≤–µ–¥–µ–Ω–∏–µ –º–æ–¥–µ–ª–∏
        user_prompt: –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–∏–π –∑–∞–ø—Ä–æ—Å —Å –¥–∞–Ω–Ω—ã–º–∏ –≤–∞–∫–∞–Ω—Å–∏–∏ –∏ —Ä–µ–∑—é–º–µ

    Returns:
        –¢–µ–∫—Å—Ç–æ–≤—ã–π –æ—Ç–≤–µ—Ç –æ—Ç –º–æ–¥–µ–ª–∏ GPT

    Raises:
        OpenAIError: –ü—Ä–∏ –æ—à–∏–±–∫–∞—Ö API
    """
    response = client.chat.completions.create(
        model="gpt-4o-mini",  # –ò—Å–ø–æ–ª—å–∑—É–µ–º —ç–∫–æ–Ω–æ–º–∏—á–Ω—É—é –º–æ–¥–µ–ª—å
        messages=[
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_prompt},
        ],
        max_tokens=1000,  # –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è –¥–ª–∏–Ω–∞ –æ—Ç–≤–µ—Ç–∞
        temperature=0,  # –î–µ—Ç–µ—Ä–º–∏–Ω–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –æ—Ç–≤–µ—Ç (–±–µ–∑ –∫—Ä–µ–∞—Ç–∏–≤–Ω–æ—Å—Ç–∏)
    )
    return response.choices[0].message.content


def validate_url(url: str, expected_type: str) -> bool:
    """
    –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ URL –≤–∞–ª–∏–¥–Ω–æ–π —Å—Å—ã–ª–∫–æ–π –Ω–∞ hh.ru.

    Args:
        url: URL –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏
        expected_type: –û–∂–∏–¥–∞–µ–º—ã–π —Ç–∏–ø ('vacancy' –∏–ª–∏ 'resume')

    Returns:
        True –µ—Å–ª–∏ URL –≤–∞–ª–∏–¥–µ–Ω, False –≤ –ø—Ä–æ—Ç–∏–≤–Ω–æ–º —Å–ª—É—á–∞–µ
    """
    if not url or not url.strip():
        return False

    url = url.strip().lower()

    if expected_type == 'vacancy':
        return 'hh.ru/vacancy/' in url or 'hh.kz/vacancy/' in url
    elif expected_type == 'resume':
        return 'hh.ru/resume/' in url or 'hh.kz/resume/' in url

    return False


